
public class ShippingMethod {
	
	private int shippingFees;

	public ShippingMethod(int shippingFees) {
		super();
		this.shippingFees = shippingFees;
	}


	public int getShippingFees() {
		return shippingFees;
	}

	public void setShippingFees(int shippingFees) {
		this.shippingFees = shippingFees;
	}
}
